package Guia7.actividad2;

public class Calculadora_01 {

    public double result;

    public void calcularSuma(double numeroUno, double numeroDos) {
        result = numeroUno + numeroDos;
    }

    public void calcularResta(double numeroUno, double numeroDos) {
        result = numeroUno - numeroDos;
    }

    public void calcularMult(double numeroUno, double numeroDos) {
        result = numeroUno * numeroDos;
    }

    public void calcularDiv(double numeroUno, double numeroDos) {
        result = numeroUno / numeroDos;
    }
}

